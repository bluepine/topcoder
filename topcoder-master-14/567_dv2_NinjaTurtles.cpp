class NinjaTurtles {
public:
	int countOpponents(int, int);
};

int NinjaTurtles::countOpponents(int P, int K) {
	int result = -1;
	int maxNum = 100000000;
	for(int i=1; i<=maxNum; i++) {
		if(P == i/K*3+i/3){
			result = i;
			break;
		}
	}
	return result;
}


