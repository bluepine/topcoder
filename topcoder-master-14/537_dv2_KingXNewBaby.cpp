string KingXNewBaby::isValid(string name) {

	string result = "YES";
	string vowels("aeiuo"); 
	map<char,int> words;
	int len = name.size();

	if(name.size() != 8) return "NO";
	char n ;
	for(int i =0; i<len; i++) {
		if(vowels.find(name[i], 0) != string::npos ) {
			words[name[i]]++;
			n = name[i];
		}	
	}

	if(words.size()!= 1 || words[n] != 2) result = "NO";
	return result;
}


