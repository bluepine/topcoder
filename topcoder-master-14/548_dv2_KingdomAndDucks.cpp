class KingdomAndDucks {
public:
	int minDucks(vector <int>);
};

int KingdomAndDucks::minDucks(vector <int> duckTypes) {
	int maxNum = 0;
	map<int, int> ducks;

	for(int i = 0; i< duckTypes.size(); i++) {
		ducks[duckTypes[i]]++;

		if (ducks[duckTypes[i]] > maxNum)  maxNum = ducks[duckTypes[i]];
	}
	

	return ducks.size() * maxNum;

}


