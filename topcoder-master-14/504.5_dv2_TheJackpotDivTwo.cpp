class TheJackpotDivTwo {
public:
	vector <int> find(vector <int>, int);
};

vector <int> TheJackpotDivTwo::find(vector <int> money, int jackpot) {
	vector<int> result(money);
	sort(result.begin(), result.end()) ;
	
	for(int i=0;i<jackpot; i++) {
		result[0]++;
		sort(result.begin(),result.end());
	}
	
	return result;
}


