int isTuples(string S, string substr, int pos) {
	int result = 0;
	int tempPos = S.find(substr, pos);

	while(1){
		if(tempPos != string::npos && S.size() - tempPos > substr.size()) result++;
		else   break;

		tempPos = S.find(substr, tempPos+1);
	}

	return result;
}
int AkariDaisukiDiv2::countTuples(string S) {	
	int result = 0;
	int len = S.size();

	if(len > 4) {
		for(int i = 1; i<len- 3; i++) 
				for (int j = 1; j<=(len-i)/2; j++)
					result += isTuples(S,S.substr(i, j), i+j+1);
			
	} 
	
	return result;
}


