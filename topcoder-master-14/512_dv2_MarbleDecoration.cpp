class MarbleDecoration {
public:
	int maxLength(int, int, int);
};

int MarbleDecoration::maxLength(int R, int G, int B) {
	int a[3] = {R,G,B};
	sort(a, a+3);
	int first = a[2];
	int second = a[1];
	int result = 0;
	
	if(first == second) result = 2*second;
	else result = 2*min(first, second) +1;
	
	return result;
}


