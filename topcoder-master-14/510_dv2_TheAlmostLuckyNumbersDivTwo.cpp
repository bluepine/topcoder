class TheAlmostLuckyNumbersDivTwo {
public:
	int find(int, int);
};
bool isAlmostLuckyNumber(int i) {
	int temp = i;
	int times = 0;
	bool result = true;
	while(temp >0) {
		if(temp%10 != 4 && temp %10 != 7){
			times++;
			if(times > 1) {
				result = false;
				break;
			}
		}
		temp /= 10;
	}
	
	return result;
}
int TheAlmostLuckyNumbersDivTwo::find(int a, int b) {
	int result = 0;
	
	for(int i=a; i<=b; i++) {
		if(isAlmostLuckyNumber(i)) result++;
	}
	
	return result;
}


