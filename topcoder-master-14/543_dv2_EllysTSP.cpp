class EllysTSP {
public:
	int getMax(string);
};

int EllysTSP::getMax(string places) {
	map<char, int> tempPlace;
	
	for(int i =0; i<places.size(); i++) {
		tempPlace[places[i]]++;
	}
	
	if(tempPlace['V'] == tempPlace['C']) return tempPlace['V'] * 2 ;
	else return min(tempPlace['C'], tempPlace['V']) *2 +1;
	
}


double test0() {
	string p0 = "CVVVC";
	EllysTSP * obj = new EllysTSP();
	clock_t start = clock();
	int my_answer = obj->getMax(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 5;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
