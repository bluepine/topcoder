class WhichDay {
public:
	string getDay(vector <string>);
};

string WhichDay::getDay(vector <string> notOnThisDay) {
	string Days[7] = { "Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"};
	string meetingDay = "";
	
	for(int i=0; i<7; i++) {
		if(notOnThisDay.end() == find(notOnThisDay.begin(), notOnThisDay.end(), Days[i])) {
			meetingDay = Days[i];
			break;
		}
	}
	
	return meetingDay;
}


