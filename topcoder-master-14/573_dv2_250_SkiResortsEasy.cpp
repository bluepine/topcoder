class SkiResortsEasy {
public:
	int minCost(vector <int>);
};

int SkiResortsEasy::minCost(vector <int> altitude) {
	int cost = 0;
	vector<int> temp(1,altitude[0]);
	for(int i=1; i<int(altitude.size()); i++) {
		if(altitude[i] > temp[i-1]) {
			temp.push_back(temp[i-1]);
			cost += altitude[i] - temp[i-1];
		}else temp.push_back(altitude[i]);
	}
	
	return cost;
	
}


