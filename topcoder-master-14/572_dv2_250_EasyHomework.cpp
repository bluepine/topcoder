	int t0[] = {5, 7, 2};
	vector <int> p0(t0, t0+sizeof(t0)/sizeof(int));
	EasyHomework * obj = new EasyHomework();
	clock_t start = clock();
	string my_answer = obj->determineSign(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "POSITIVE";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
