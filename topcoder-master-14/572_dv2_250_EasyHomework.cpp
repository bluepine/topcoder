class EasyHomework {
public:
	string determineSign(vector <int>);
};

string EasyHomework::determineSign(vector <int> A) {
	bool hasZero = false;
	int isNegative = 1;
	
	for(int i=0; i<int(A.size()); i++) {
		if(A[i] == 0) {
			hasZero = true;
			break;
		}else if(A[i] < 0) {
			isNegative *= -1;
		}
	}
	if(hasZero) return "ZERO";
	else if(isNegative == -1) return "NEGATIVE";
	else return "POSITIVE";
}


