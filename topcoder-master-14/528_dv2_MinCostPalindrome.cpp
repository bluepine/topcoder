class MinCostPalindrome {
public:
	int getMinimum(string, int, int);
};

int MinCostPalindrome::getMinimum(string s, int oCost, int xCost) {
	
	int result = 0;
	for(int i=0, j=s.size()-1; i<j; i++, j--) {
		if(s[i] != s[j] ) {
			if(s[i] !='?' && s[j] != '?') {  result = -1;	break; }
			else if((s[i] == '?' && s[j] == 'o') || (s[i] == 'o' && s[j] == '?')) result += oCost;
			else if((s[i] == '?' && s[j] == 'x') || (s[i] == 'x' && s[j] == '?')) result += xCost;
		}else if(s[i] == s[j] && s[i] == '?') result += min(oCost, xCost)*2;
	}
	
	return result;
	
}


