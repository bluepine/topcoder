class NetworkXZeroOne {
public:
	string reconstruct(string);
};

string NetworkXZeroOne::reconstruct(string message) {
	int temp = 0;
	string result(message);
	for(int i=0; i<message.size(); i++) {
		if(message[i] != '?') { temp = i; break;}
	}		
	
	for(int i = temp-1; i>=0; i--) {
		if(result[i+1] == 'x') result[i] = 'o';
		else result[i] = 'x';
	}
	
	for(int i=temp+1; i<message.size(); i++) {
		if(result[i-1] == 'x') result[i] = 'o';
		else result[i] = 'x';
	}
	
	return result;
}


double test0() {
	string p0 = "x?x?";
	NetworkXZeroOne * obj = new NetworkXZeroOne();
	clock_t start = clock();
	string my_answer = obj->reconstruct(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "xoxo";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
