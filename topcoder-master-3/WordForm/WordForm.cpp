class WordForm {
public:
    string getSequence(string word) {
        transform(word.begin(), word.end(), word.begin(), ::toupper);
        string res = "";

        bool isV = false;
        if (isVowel(word[0])) {
            res.push_back('V');
            isV = true;
        }
        else
            res.push_back('C');

        for (int i = 1; i < (int)word.size(); ++i) {
            if (isVowel(word[i]) || (word[i] == 'Y' && !isV)) {
                if (!isV) {
                    res.push_back('V');
                }
                isV = true;
            }
            else{
                if (isV) {
                    res.push_back('C');
                }
                isV = false;
            }
        }

        return res;
    }

    bool isVowel(char c) {
        return (c == 'A' || c == 'E' || c == 'I' || c == 'O' || c == 'U');
    }
};


