class Mortgage {
public:
    int monthlyPayment(int l, int i, int t) {
        t *= 12;
        int lo = l/t, hi = l;
        while (lo < hi) {
            int mid = lo+(hi-lo)/2;
            if (check(l, i, t, mid)) hi = mid;
            else lo = mid+1;
        }

        return lo;
	}

    bool check(int l, int i, int t, int m) {
        double r = 1.0+i/1000.0/12;
        while (t > 0) {
            l -= m;
            if (l <= 0) return true;
            l = ceil(l*r);
            t--;
        }
        return false;
    }
};


