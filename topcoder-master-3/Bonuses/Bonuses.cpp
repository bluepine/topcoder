class Bonuses {
public:
    struct cmp {
        bool operator() (pair<int, int> a, pair<int, int> b) {
            if (a.first == b.first) {
                return a.second < b.second;
            }
            return a.first > b.first;
        }
    };

    vector <int> getDivision(vector <int> points) {
        int N = points.size();
        if (N == 0) return vector<int>();
        int sum = 0;
        vector<pair<int, int> > tmp;
        for (int i = 0; i < N; i++) {
            sum += points[i];
            tmp.push_back(make_pair(points[i], i));
        }
        sort(tmp.begin(), tmp.end(), cmp());
        int remain = 100;
        vector<int> res(N, 0);
        for (int i = 0; i < N; i++) {
            int bonus = 100.0*tmp[i].first/sum;
            res[tmp[i].second] = bonus;
            remain -= bonus;
        }
        int i = 0;
        while (remain-- > 0) {
            res[tmp[i++].second]++;
        }
        return res;
    }
};


