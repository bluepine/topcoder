class HowEasy {
public:
    int pointVal(string param0) {
        istringstream stream(param0);
        string str;
        int count = 0, len = 0;
        while (stream >> str) {
            int N = str.size();
            if (str[N-1] == '.') N -= 1;
            if (N == 0) continue;
            int i = 0;
            for(i = 0; i < N; i++)
                if ((str[i] < 'a' || str[i] > 'z') && (str[i] < 'A' || str[i] > 'Z'))
                    break;
            if (i == N) {
                len += N;
                count++;
            }
        }
        if (count == 0) return 250;
        len /= count;
        if (len <= 3) return 250;
        if (len >= 6) return 1000;
        return 500;
    }
};


