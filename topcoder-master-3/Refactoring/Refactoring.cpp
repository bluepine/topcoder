class Refactoring {
public:
    int refactor(int n) {
        return go(2, n);	
    }

    int go(int l, int n) {
        int res = 0;
        for (int i = l; i*i <= n; i++) {
            if (n%i == 0) {
                res++;
                res += go(i, n/i);
            }
        }
        return res;
    }
};


